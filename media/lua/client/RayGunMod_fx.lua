require "lua_timers"
RayGunMod = RayGunMod or {}
RayGunMod.effects = {}
RayGunMod.dbg = false

--[[
function RayGunMod.isShowIndicator()
    local CircleChance = SandboxVars.RayGunMod.CircleChance or 15
    return RayGunMod.doRoll(CircleChance)
end
 ]]


function RayGunMod.startEffectLoop(sq)
    if not sq then return end
    local pl = getPlayer()
    local rep = 0

    RayGunMod.clearAllEffects()
    local function loopEffect()
        rep = rep + 1

        if sq then
            local csq = pl:getCurrentSquare()

            if csq and csq == sq then
                if rep >= 300 then
                    RayGunMod.stopEffectLoop(sq, false)
                else
                    RayGunMod.stopEffectLoop(sq, true)
                end
                RayGunMod.clearAllEffects()
                return
            end

            if rep >= 300 then
                RayGunMod.stopEffectLoop(sq, false)
                RayGunMod.clearAllEffects()
            elseif rep <= 300 then
                for i = 1, 9 do
                    RayGunMod.blink(sq, i)
                end
                RayGunMod.effects[sq] = timer:Simple(0.2, loopEffect)
            else
                RayGunMod.stopEffectLoop(sq, false)
                RayGunMod.clearAllEffects()
            end

        else
            RayGunMod.clearAllEffects()
        end
    end

    loopEffect()

    if getCore():getDebug() and RayGunMod.dbg then
        print(rep)
    end

end

function RayGunMod.clearAllEffects()
    for activeSq, _ in pairs(RayGunMod.effects) do
        RayGunMod.stopEffectLoop(activeSq, false)
    end
    RayGunMod.effects = {}
end

function RayGunMod.stopEffectLoop(sq)
    local pl = getPlayer()
    if sq then
        if RayGunMod.effects[sq] then
            timer:Remove(RayGunMod.effects[sq])
            RayGunMod.effects[sq] = nil
        end
    end
    RayGunMod.clearAllEffects()
end



function RayGunMod.doEffect(sq)
    --if not RayGunMod.isShowIndicator() then return end
    local lightFX = IsoLightSource.new(sq:getX(), sq:getY(), sq:getZ(), 0, 5, 0, 4)
    getCell():addLamppost(lightFX)

    local delay = 0.1
    if sq then
        local m = getWorldMarkers():addGridSquareMarker("circle_center", "circle_only_highlight", sq, 0, 1, 0, true, 2)
        delay = delay + 0.1
        timer:Simple(delay, function()
            m:remove()
            timer:Simple(delay, function()
                local m1 = getWorldMarkers():addGridSquareMarker("circle_center", "circle_only_highlight", sq, 0, 1, 0, true, 2)
                delay = delay + 0.1
                timer:Simple(delay, function()
                    m1:remove()
                    getCell():removeLamppost(lightFX);
                end)
            end)
        end)
    end
end

