require "lua_timers"

RayGunMod = RayGunMod or {}
local Commands = {};
Commands.RayGunMod = {};

Commands.RayGunMod.syncBurst = function(args)
    local source = getPlayer();
    local player = getPlayerByOnlineID(args.id)
    RayGunMod.doBurst(args.x, args.y, args.z, args.dir)
    if source ~= player then
        local sq = getCell():getOrCreateGridSquare(args.x, args.y, args.z)
        RayGunMod.AoE(sq, false)
        RayGunMod.doEffect(sq)
    end
end

Events.OnServerCommand.Add(function(module, command, args)
	if Commands[module] and Commands[module][command] then
		Commands[module][command](args)
	end
end)

